-- 创建论坛帖子表
USE majors_db;
CREATE TABLE IF NOT EXISTS forum_posts (
    post_id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    name VARCHAR(50) NOT NULL,
    school VARCHAR(100),
    major VARCHAR(100),
    category_id INT NOT NULL COMMENT '帖子所属专业类别ID',
    post_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    content TEXT NOT NULL,
    comments TEXT,
    FOREIGN KEY (user_id) REFERENCES users(user_id) ON DELETE CASCADE,
    FOREIGN KEY (category_id) REFERENCES major_categories(category_id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 添加索引
CREATE INDEX idx_user_id ON forum_posts(user_id);
CREATE INDEX idx_post_time ON forum_posts(post_time);
CREATE INDEX idx_category_id ON forum_posts(category_id);

